function Content(id,name,description,path,categories,created) {
    this.id = id;
    this.name = name;
    this.description = description;
    this.path = path;
    this.categories = categories;
    this.created = created;
}

Content.prototype.id;
Content.prototype.name;
Content.prototype.description;
Content.prototype.path;
Content.prototype.categories;
Content.prototype.created;
